 const add = (num1,num2) => {
     return num1 + num2
 }

 const subtract =(num1, num2) => {
     return num1 - num2; 
 }

 const multiply = (num1,num2) => {
     return num1 * num2
 }

 const divide = (num1, num2) => {
     return num1 / num2
 }

 const increment = (n) => {
     return n +=1;
 }

 const decrement = (n) => {
     return n -=1;
 }

const makeInt = (n) => {
    return parseInt(n, 10)
}

const preserveDecimal = (n) => {
    return parseFloat(n)
}